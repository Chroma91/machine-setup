    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="machine_setup"
                ;;
            machine_setup,help)
                cmd="machine_setup__help"
                ;;
            machine_setup,install)
                cmd="machine_setup__install"
                ;;
            machine_setup,list)
                cmd="machine_setup__list"
                ;;
            machine_setup,uninstall)
